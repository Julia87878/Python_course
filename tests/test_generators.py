@pytest.mark.parametrize(
    "start, stop, expected",
    [
        (5, 7, ["0000 0000 0000 0005", "0000 0000 0000 0006", "0000 0000 0000 0007"]),
        (13345879, 13345880, ["0000 0000 1334 5879", "0000 0000 1334 5880"]),
        (2233123412341224, 2233123412341225, ["2233 1234 1234 1224", "2233 1234 1234 1225"]),
        (7686569809878878, 76865698098788799, ["Ошибка ввода"]),
    ],
)
def test_card_number_generator_1(start, stop, expected):
    generator = list(card_number_generator(start, stop))
    assert generator == expected


def test_card_number_generator_2():
    generator_5 = card_number_generator(0, 5)
    assert next(generator_5) == "Ошибка ввода"


def test_filter_by_currency_2():
    generator_3 = filter_by_currency([], "USD")
    assert next(generator_3) == "Нет транзакций"
